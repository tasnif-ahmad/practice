let a = 5; // 0101
let b = 3; // 0011

console.log("a =", a);
console.log("b =", b);

console.log("a & b =", a & b);   // AND
console.log("a | b =", a | b);   // OR
console.log("a ^ b =", a ^ b);   // XOR
console.log("~a =", ~a);         // NOT
console.log("a << 1 =", a << 1); // Left shift
console.log("a >> 1 =", a >> 1); // Right shift
